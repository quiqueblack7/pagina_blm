<div id="texto6" align="justify"> Super Vía Santa Fe Edo. de México </div>
<br><br>
<div id="texto4" align="justify">
Esta supercarretera, de 5 km de longitud, conectará a Santa Fe con el Distrito Federal y el Periférico elevado, con una inversión de 6 mil millones de pesos. La parte solar iluminada de la autopista (2km) incluye carreteras elevadas y puentes que son lugares ideales para la reducción de costos cuando se emplea energía solar a través de aparatos de iluminación alimentados por CA.
<br><br>
La decisión de emplear sistemas solares EG 500, al proyecto de esta autopista aportó un beneficio de ahorro en costos como resultado de una instalación libre de cableado y un programa de mantenimiento de bajo costo.
En combinación con el uso de cero energía, es por eso que nos unimos nuevamente al compromiso con México de dar soluciones sustentables en el desarrollo proyectos de infraestructura.
<br><br>
Este proyecto fue valorado en aproximadamente $ 500,000 USD, los EG500 sistemas solares de iluminación al aire libre fueron seleccionados nuevamente por las autoridades, gracias a su bajo costo y buen funcionamiento del sistema.
